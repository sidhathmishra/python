# convert to int
print(int(3.14))#converting to int
a = int('485')#numeric string to int
b = int('768')#numeric string to int
c = a + b
print(c)

print(int('1011',2))#binary to decimal
print(int('361',8))#octal to decimal
print(int('21',16))#hex to decimal 

#convert to float 
print(float(35))#int to float
i = float('4.85')#numeric string to float
j = float('7.68')#numeric string to float
print(i+j)

#convert to complex 
print(complex(35))#int to float
x = complex(4.85,1.1)#string to float
y = complex(7.68,2.1)#string to float
print(x+y)

#convert to bool
print(bool(35))
print(bool(1.2))
print(int(True))
print(int(False))
