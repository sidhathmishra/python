#string conversions 
message = "Rust"
print(message)
print(str.upper(message))#converting to uppercase
print(str.lower(message))#converting to Lowercase
print(str.capitalize(message))#converting first letter to capital 
print(str.title(message))#converting first letter to capital
print(str.swapcase(message))#swaping cases in string
